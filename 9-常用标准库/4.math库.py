import math

print(math.ceil(5.5))  # math.ceil(5.5) 大于5.5的最小整数，就是想上取整   6
print(math.floor(5.5)) # math.floor(5.5) 小于5.5的最大整数，向下取整  5
print(math.sqrt(16))   # 对16取开方跟  4.0

